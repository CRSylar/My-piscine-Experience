int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int n;

	i = 0;
	n = 0;
	while (s1[i] || s2[i])
	{
		n = ((unsigned char)s1[i] - (unsigned char)s2[i]);
		if (n == 0)
			i++;
		else
			return (n);
	}
	return (n);
}
