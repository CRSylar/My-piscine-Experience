void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	temp;
	int	sz;

	sz = size;
	while (sz > 0)
	{
		i = 0;
		while (tab[i] && (size - 1 > i))
		{
			if (tab[i] > tab[i + 1])
			{
				temp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = temp;
			}
			i++;
		}
		sz--;
	}
}
